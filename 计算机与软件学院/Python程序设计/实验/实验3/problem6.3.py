t = (1, 2, 3, 7, 9, 0, 5)
print("t[3] = ", t[3])
print("t[1: 3] = ", t[1: 3])
print("t[-1:-3] = ", t[-1:-3])
print("t[-1:-3:-1] = ", t[-1:-3:-1])
print("t[-1:: -3] = ", t[-1:: -3])
print("t[: -1: 3] = ", t[: -1: 3])
print("t[3: -1: 3] = ", t[3: -1: 3])
print("t[3: -1: -3] = ", t[3: -1: -3])
